    // Start is called before the first frame update
    void Start()
    {
        displaySuitPortrait(0.025f);
    }

    // Update is called once per frame
    void Update()
    {
        timer+=Time.deltaTime;
        if(timer>=timePerLine && line<suitPortrait.Length){
            displayed+=suitPortrait[line]+'\n';
            timer=0;
            line++;

        }
        if(timer>=4){
            SceneManager.LoadScene(2);
        }
        portraitDisplay.text=displayed;

    }



    //Parse a given path into a string
    public void displaySuitPortrait(float t)
    {
        timePerLine=t;
        timer=0;
        line=0;
        displayed="";

